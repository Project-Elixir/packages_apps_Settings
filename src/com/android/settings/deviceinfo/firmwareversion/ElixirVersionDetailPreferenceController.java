public class ElixirVersionDetailPreferenceController extends BasePreferenceController {

    private static final String TAG = "ElixirVersionDialogCtrl";
    private static final int DELAY_TIMER_MILLIS = 500;
    private static final int ACTIVITY_TRIGGER_COUNT = 3;

    private static final String KEY_ELIXIR_VERSION_PROP = "org.elixir.version";

    public ElixirVersionDetailPreferenceController(Context context, String key) {
        super(context, key);
    }

    @Override
    public int getAvailabilityStatus() {
        return AVAILABLE;
    }

    @Override
    public CharSequence getSummary() {
        return SystemProperties.get(KEY_ELIXIR_VERSION_PROP,
                mContext.getString(R.string.unknown));
    }
}
